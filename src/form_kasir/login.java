    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String nama;
String id;
    if (us.getText().equals("") || pw.getText().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Nama atau password tidak boleh kosong", "Error", 1);
    } else { 
        try {
            Connection kon = KoneksiDatabase.getConnection();    
            Statement st = kon.createStatement();
            String login = "SELECT * FROM kasir WHERE nm_kasir = '" + us.getText() + "' AND password = '" + pw.getText() + "'";
            ResultSet rs = st.executeQuery(login);

            if (rs.next()) {
                nama = rs.getString("nm_kasir");
                id = rs.getString("id_kasir");// ambil nama dari database

                // Kirim nama ke form home
                home hm = new home(nama, id);
                hm.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Username atau password salah", "Login Gagal", 2);
            }

            // Tutup koneksi
            rs.close();
            st.close();
            kon.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Terjadi kesalahan: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

